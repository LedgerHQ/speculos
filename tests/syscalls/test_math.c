void test_math(void **state __attribute__((unused)))
{
  uint8_t r[64], r128[128];
  int carry;

  /* a + b */
  carry = sys_cx_math_add(r, a, b, sizeof(r));
  assert_int_equal(carry, 0);
  assert_memory_equal(r, radd, sizeof(radd));

  /* a + b with overflow */
  carry = sys_cx_math_add(r, a2, p2, sizeof(a2));
  assert_int_equal(carry, 1);
  assert_memory_equal(r, radd_overflow, sizeof(radd_overflow));

  /* (a + b) % m */
  sys_cx_math_addm(r, a, b, m, sizeof(r));
  assert_memory_equal(r, raddm, sizeof(raddm));

  /* a % m */
  memcpy(r, a, sizeof(r));
  sys_cx_math_modm(r, sizeof(r), m, sizeof(r));
  assert_memory_equal(r, rmodm, sizeof(rmodm));

  /* (a * b) */
  sys_cx_math_mult(r128, a, b, sizeof(a));
  assert_memory_equal(r128, rmult, sizeof(rmult));

  /* (a * b) % m */
  sys_cx_math_multm(r, a, b, m, sizeof(r));
  assert_memory_equal(r, rmultm, sizeof(rmultm));

  /* pow(a, b, m) */
  sys_cx_math_powm(r, a, b, sizeof(b), m, sizeof(r));
  assert_memory_equal(r, rpowm, sizeof(rpowm));

  /* a - b */
  sys_cx_math_sub(r, a, b, sizeof(r));
  assert_memory_equal(r, rsub, sizeof(rsub));

  /* (a - b) % m */
  sys_cx_math_subm(r, a, b, m, sizeof(r));
  assert_memory_equal(r, rsubm, sizeof(rsubm));

  /* modinv(0x10001, p1) */
  sys_cx_math_invintm(r, 0x10001, p1, sizeof(p1));
  assert_memory_equal(r, rinvintm, sizeof(rinvintm));

  /* modinv(a2, p2) */
  sys_cx_math_invprimem(r, a2, p2, sizeof(p2));
  assert_memory_equal(r, rinvprimem, sizeof(rinvprimem));
}

int main(void)
{
  const struct CMUnitTest tests[] = {
    cmocka_unit_test(test_math),
  };

  return cmocka_run_group_tests(tests, NULL, NULL);
}
